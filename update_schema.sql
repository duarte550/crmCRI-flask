
-- CRM Schema Update Script
-- This script updates an existing database schema to the latest version.
-- It is designed to be safely re-runnable on Databricks SQL.
-- It uses `ADD COLUMN IF NOT EXISTS` and `CREATE TABLE IF NOT EXISTS` to prevent errors.

USE cri_cra_dev.crm;

-- ====================================================================
-- Tabela: operations
-- Garante que a tabela principal de operações tenha todas as colunas necessárias.
-- ====================================================================
